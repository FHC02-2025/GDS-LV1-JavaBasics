public class ArithmetischeOperationen {

    public static void main(String[] args) {


        // addieren -> +
        // subtrahieren -> -
        // multiplizieren -> *
        // dividieren -> /
        // modulo (Restwert) -> %

        int a = 4;
        int b = 3;

        System.out.println(a + b); // STRG + D -> duplizieren
        System.out.println(a - b); // STRG + D -> duplizieren
        System.out.println(a * b); // STRG + D -> duplizieren
        System.out.println(a / b); // STRG + D -> duplizieren
        System.out.println(a % b); // STRG + D -> duplizieren

    }
}
